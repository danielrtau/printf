#include "ft_printf.h"

int	ft_putnbr(int n)
{
	char	a[10];
	int		b;
	int		c;

	c = 0;
	b = -1;
	if (n == -2147483648)
		c += ft_putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			c += ft_putchar('-');
			n *= -1;
		}
		while (n / 10)
		{
			a[++b] = n % 10;
			n /= 10;
		}
		a[++b] = n;
		while (b >= 0)
			c += ft_putchar(a[b--] + '0');
	}
	return (c);
}
