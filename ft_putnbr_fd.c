#include "ft_printf.h"

int	ft_putnbr_fd(int n)
{
	char	a[10];
	int		b;
	int		c;

	c = 0;
	b = -1;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		c += 11;
	}
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			c++;
			n *= -1;
		}
		while (n / 10)
		{
			a[++b] = n % 10;
			n /= 10;
		}
		a[++b] = n;
		while (b >= 0)
		{
			c += ft_putchar_fd(a[b] + '0');
			b--;
		}
	}
	return(c);
}
